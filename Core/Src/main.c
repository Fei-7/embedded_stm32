void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
	if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)  // if the interrupt source is channel1
	{
		if (Is_first_Captured1==0) // if the first value is not captured
		{
			IC_Val11 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1); // read the first value
			Is_first_Captured1 = 1;  // set the first captured as true
			// Now change the polarity to falling edge
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_FALLING);
		}

		else if (Is_first_Captured1==1)   // if the first is already captured
		{
			IC_Val12 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);  // read second value
			__HAL_TIM_SET_COUNTER(htim, 0);  // reset the counter

			if (IC_Val12 > IC_Val11)
			{
				Difference1 = IC_Val12-IC_Val11;
			}

			else if (IC_Val11 > IC_Val12)
			{
				Difference1 = (0xffff - IC_Val11) + IC_Val12;
			}

			Distance1 = Difference1 * .034/2;
			Is_first_Captured1 = 0; // set it back to false
			Difference1 = 0;
//			HAL_UART_Transmit(&huart2, Distance1, sizeof (Distance1), 10);
//		    tm1637DisplayDecimal(Distance1, 0);
			// set polarity to rising edge
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_RISING);
			__HAL_TIM_DISABLE_IT(&htim3, TIM_IT_CC1);
		}
	}

	if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_2)  // if the interrupt source is channel1
	{
		if (Is_first_Captured2==0) // if the first value is not captured
		{
			IC_Val21 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_2); // read the first value
			Is_first_Captured2 = 1;  // set the first captured as true
			// Now change the polarity to falling edge
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_2, TIM_INPUTCHANNELPOLARITY_FALLING);
		}

		else if (Is_first_Captured2==1)   // if the first is already captured
		{
			IC_Val22 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_2);  // read second value
			__HAL_TIM_SET_COUNTER(htim, 0);  // reset the counter

			if (IC_Val22 > IC_Val21)
			{
				Difference2 = IC_Val22-IC_Val21;
			}

			else if (IC_Val21 > IC_Val22)
			{
				Difference2 = (0xffff - IC_Val21) + IC_Val22;
			}

			Distance2 = Difference2 * .034/2;
			Is_first_Captured2 = 0; // set it back to false

			Difference2 = 0;

//			HAL_UART_Transmit(&huart2, Distance2, sizeof (Distance2), 10);
//			tm1637DisplayDecimal(Distance2, 0);
			// set polarity to rising edge
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_2, TIM_INPUTCHANNELPOLARITY_RISING);
			__HAL_TIM_DISABLE_IT(&htim3, TIM_IT_CC2);
		}
	}
}

void HCSR04_Read (void)
{
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_7, GPIO_PIN_SET);  // pull the TRIG pin HIGH
	HAL_Delay(15);  // wait for 10 us
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_7, GPIO_PIN_RESET);

	__HAL_TIM_ENABLE_IT(&htim3, TIM_IT_CC1);

	HAL_GPIO_WritePin(GPIOB, GPIO_PIN_4, GPIO_PIN_SET);  // pull the TRIG pin HIGH
	HAL_Delay(15);  // wait for 10 us
	HAL_GPIO_WritePin(GPIOB, GPIO_PIN_4, GPIO_PIN_RESET);

	__HAL_TIM_ENABLE_IT(&htim3, TIM_IT_CC2);
}
